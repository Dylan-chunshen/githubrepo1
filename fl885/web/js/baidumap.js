//创建一个Icon
function createIcon(json){
	var icon = new BMap.Icon("http://app.baidu.com/map/images/us_mk_icon.png", new BMap.Size(json.w,json.h),{imageOffset: new BMap.Size(-json.l,-json.t),infoWindowOffset:new BMap.Size(json.lb+5,1),offset:new BMap.Size(json.x,json.h)})
	return icon;
}

/** 地图上弹出显示框 **/
function openMapDialog(point,content){
	// 变量准备
	if(point==null){point = new BMap.Point(121.473239,31.22199);}
	var iconImg = createIcon({w:23,h:25,l:46,t:21,x:9,lb:12});
	var label = new BMap.Label("",{"offset":new BMap.Size(13,-20)});
	label.setStyle({borderColor:"#808080",color:"#333",cursor:"pointer"});
	var i_iw = new BMap.InfoWindow("<div class='iw_poi_content'>"+content+"</div>");
	
	// 1.清除其它点；2.设置中心；3.生成marker 4.地图上显示marker点 5.marker弹出对话框
	map.clearOverlays(); 
	map.centerAndZoom(point,13);
	var marker = new BMap.Marker(point,{icon:iconImg});
	marker.setLabel(label);
	map.addOverlay(marker);
	marker.openInfoWindow(i_iw);	
}
